# Function to remove a product from the cart
def remove_from_cart():
    if not cart_items:
        print("Cart is empty.")
        return

    print("\n=== REMOVE ITEM FROM CART ===")

    for i, (name, qty) in enumerate(zip(cart_items, cart_quantities), 1):
        print(f"{i}. {name} (Qty: {qty})")

    name = input("Enter product name to remove: ").strip()

    if name not in cart_items:
        print("Product not found in cart.")
        return

    index = cart_items.index(name)
    current_qty = cart_quantities[index]

    try:
        remove_qty = int(input("Enter quantity to remove: "))

        if remove_qty <= 0:
            print("Invalid quantity.")
            return

        if remove_qty > current_qty:
            print("You cannot remove more than existing quantity.")
            return

        cart_quantities[index] -= remove_qty
        product_stock[name] += remove_qty

        if cart_quantities[index] == 0:
            cart_items.pop(index)
            cart_quantities.pop(index)
            cart_prices.pop(index)

        print(f"{remove_qty} x {name} removed from cart.")

    except ValueError:
        print("Enter valid quantity.")

# Function to update quantity of a product in the cart
def update_quantity():
    if not cart_items:
        print("Cart is empty. Nothing to update.")
        return

    print("\n=== UPDATE QUANTITY ===")
    for i, (name, qty) in enumerate(zip(cart_items, cart_quantities), 1):
        print(f"{i}. {name} (Current Qty: {qty})")

    try:
        choice = int(input("Select item number to update: "))
        if choice < 1 or choice > len(cart_items):
            print("Invalid selection.")
            return

        index = choice - 1
        name = cart_items[index]
        current_qty = cart_quantities[index]

        new_qty = int(input("Enter new quantity: "))
        if new_qty <= 0:
            print("Quantity must be positive.")
            return

        # Restore previous quantity to stock
        product_stock[name] += current_qty

        if new_qty > product_stock[name]:
            print("Not enough stock available.")
            product_stock[name] -= current_qty
            return

        cart_quantities[index] = new_qty
        product_stock[name] -= new_qty

        print(f"Quantity updated successfully for {name}.")

    except ValueError:
        print("Invalid input.")

